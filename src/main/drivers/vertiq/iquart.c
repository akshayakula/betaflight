/*
 * iquartBuildSetFloat
 *
 * Frame layout (11 bytes total):
 *   [0]    0x55                      start-of-frame
 *   [1]    0x06                      payload length (6 bytes follow)
 *   [2]    0x34                      type_idn (PropellerMotorControl = 52)
 *   [3]    0x05                      sub_idn (ctrl_velocity = 5)
 *   [4]    0x01                      reserved/subindex
 *   [5..8] float_le                  velocity in rad/s, little-endian
 *   [9..10] crc16_le                 CRC-16, little-endian
 *
 * CRC is computed over bytes [2..8] (type, sub, reserved, float[4]).
 */

uint8_t iquartBuildSetFloat(uint8_t *buf, float value)
{
    buf[0] = 0x55;
    buf[1] = 0x06;                              /* payload length */
    buf[2] = IQUART_PROP_MOTOR_TYPE_IDN;        /* type_idn = 52 */
    buf[3] = IQUART_CTRL_VELOCITY_SUB_IDN;      /* sub_idn = 5 */
    buf[4] = 0x01;                              /* reserved */

    /* copy float as little-endian bytes */
    memcpy(&buf[5], &value, sizeof(float));

    /* CRC over payload bytes [2..8] */
    uint16_t crc = crc_init();
    for (int i = 2; i < 9; i++) {
        crc = crc_accumulate(crc, buf[i]);
    }
    crc = crc_finalize(crc);

    /* CRC stored little-endian */
    buf[9] = (uint8_t)(crc & 0xFF);
    buf[10] = (uint8_t)(crc >> 8);

    return IQUART_SET_FLOAT_LEN;
}

