static uint16_t crc_init(void)
{
    return 0xFFFF;
}

static uint16_t crc_accumulate(uint16_t crc, uint8_t data)
{
    uint8_t index = (uint8_t)((crc >> 8) ^ data);
    return (uint16_t)((crc << 8) ^ crc_table[index]);
}

static uint16_t crc_finalize(uint16_t crc)
{
    return crc;
}

